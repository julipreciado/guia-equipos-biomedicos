def formatear_lista(texto):

    if pd.isna(texto):
        return ""

    lineas = str(texto).split("\n")
    resultado = []

    for linea in lineas:

        linea = linea.strip()
        linea = re.sub(r'^\d+\.\s*', '', linea)

        if linea:
            resultado.append(f"- {linea}")

    return "\n".join(resultado)


def formatear_normas(texto):

    if pd.isna(texto):
        return ""

    texto = str(texto)
    partes = texto.split("*")

    intro = partes[0].strip()

    bullets = []

    for p in partes[1:]:
        p = p.strip().replace("\n", " ")

        if p:
            bullets.append(f"- {p}")

    return intro + "\n\n" + "\n".join(bullets)


def formatear_servicios(texto):

    if pd.isna(texto):
        return ""

    partes = str(texto).split(",")

    intro = partes[0].strip()

    bullets = []

    for p in partes[1:]:

        p = p.strip()

        if p:
            bullets.append(f"- {p}")

    return intro + "\n\n" + "\n".join(bullets)

