def limpiar(texto):
    texto = texto.lower().strip()
    texto = texto.replace("á","a").replace("é","e").replace("í","i")
    texto = texto.replace("ó","o").replace("ú","u")
    texto = texto.replace("ñ","n")
    texto = re.sub(r'[^a-z0-9]+', '_', texto)
    return texto

# ---------------------------------
